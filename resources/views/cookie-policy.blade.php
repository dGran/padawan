            	</li>
            	<li class="py-1">
            		<span class="font-semibold dark:text-white">Cookies de terceros:</span> Son aquéllas que se envían al equipo terminal del usuario desde un equipo o dominio que no es gestionado por el editor, sino por otra entidad que trata los datos obtenidos través de las cookies.
            	</li>
