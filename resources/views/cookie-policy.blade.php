            	</li>
            	<li class="py-1">
            		<span class="font-semibold dark:text-white">Cookies persistentes:</span> Son un tipo de cookies en el que los datos siguen almacenados en el terminal y pueden ser accedidos y tratados durante un periodo definido por el responsable de la cookie, y que puede ir de unos minutos a varios años.
